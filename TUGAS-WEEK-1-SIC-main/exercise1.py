# Tugas 1: Menyimpan dan Menampilkan Data

# Menyimpan nama produk pertama dalam variabel
nama_produk1 = "Kopi Pagi"
# Menyimpan harga produk pertama (float karena ada desimal)
harga_produk1 = 18000.5

# Menyimpan nama produk kedua dalam variabel
nama_produk2 = "Roti Cokelat"
# Menyimpan harga produk kedua (integer karena bulat)
harga_produk2 = 10000

# Menyimpan status ketersediaan roti (True artinya tersedia)
status_roti = True

# Menampilkan semua data yang sudah disimpan
print("Nama produk 1:", nama_produk1)
print("Harga produk 1:", harga_produk1)
print("Nama produk 2:", nama_produk2)
print("Harga produk 2:", harga_produk2)
print("Status ketersediaan roti:", status_roti)

'''
Nama produk 1: Kopi Pagi
Harga produk 1: 18000.5
Nama produk 2: Roti Cokelat
Harga produk 2: 10000
Status ketersediaan roti: True
'''